async function run() {
  await mongoose.connect(MONGODB_URI);
  const lines = rawText.split(/[\r\n,]+/).map(s => s.trim()).filter(Boolean);
  
  const validList = [];
  const seen = new Set();
  for (const line of lines) {
    const { isValid, normalized } = normalizePhoneNumber(line);
    if (isValid && !seen.has(normalized)) {
      seen.add(normalized);
      validList.push(normalized);
    }
  }

  const existingDocs = await VerifiedNumber.find({ phoneNumber: { $in: validList } }).select('phoneNumber').lean();
  const existingSet = new Set(existingDocs.map(d => d.phoneNumber));

  const toInsert = [];
  for (const phone of validList) {
    if (!existingSet.has(phone)) {
      toInsert.push({
        phoneNumber: phone,
        batchId: 'BATCH-INITIAL-SEED-1000',
        uploadedBy: 'System Admin',
        status: 'verified'
      });
    }
  }

  if (toInsert.length > 0) {
    await VerifiedNumber.insertMany(toInsert, { ordered: false });
  }

  await PendingNumber.updateMany(
    { phoneNumber: { $in: validList }, status: 'pending' },
    { status: 'approved', verifiedAt: new Date() }
  );

  const count = await VerifiedNumber.countDocuments();
  console.log(`DONE! Added ${toInsert.length} new numbers. Total verified numbers now in MongoDB: ${count}`);
  await mongoose.disconnect();
}

